let weekNumbersToWords = prompt("Please give a number 1-7")

switch(weekNumbersToWords) {
    case "1" :
        alert("Monday")
    break;

    case "2" :
        alert("Tuseday")
    break;

    case "3" :
        alert("Wednesday")
    break;

    case "4" :
        alert("Thursday")
    break;

    case "5" :
        alert("Friday")
    break;

    case "6" :
        alert("Saturday")
    break;

    case "7" :
        alert("Sunday")
    break;

    default:
        alert("Input error. Please write number between'1-7'")
        
}

